int		check_input(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (i % 2 == 0)
		{
			if (str[i] < '1' || str[i] > '4')
				return (0);
		}
		if (i % 2 == 1)
		{
			if (str[i] != ' ')
				return (0);
		}
		i++;
	}
	if (i == 31)
		return (1);
	else
		return (0);
}
